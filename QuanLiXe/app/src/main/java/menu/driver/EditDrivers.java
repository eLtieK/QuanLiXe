    public boolean checkForm() {
        if (ID.getText().isEmpty() || Name.getText().isEmpty() || Exper.getText().isEmpty() || PhoneNumber.getText().isEmpty() || Address.getText().isEmpty()) {
            return true;
        }
        return false;
    }

    public boolean checkSo(String str) {
        if (str == null || str.length() == 0) {
            return false;
        }
        for (char c : str.toCharArray()) {
            if (!Character.isDigit(c)) {
                return false;
            }
        }
        return true;
    }

    public void reset() {
        Name.setText("");
        PhoneNumber.setText("");
        Address.setText("");
        Exper.setText("");
        License.setSelectedIndex(0);
        ID.setText("");
    }
    public void save(){
        try {
            // TODO add your handling code here:
            if (checkForm()) {
                JOptionPane.showMessageDialog(this, "Enter complete information !");
                reset();
                return;
            } else if (!checkSo(ID.getText()) || !checkSo(Exper.getText())) {
                JOptionPane.showMessageDialog(this, "Please enter the correct format !");
                reset();
                return;
            }
            int id = Integer.parseInt(ID.getText());

            String name = Name.getText();
            String bangcap = String.valueOf(License.getSelectedItem());
            int exper = Integer.parseInt(Exper.getText());
            String phoneNumber = PhoneNumber.getText();
            String address = Address.getText();
            if (!Manager.checkValidDriver(id)) {
                JOptionPane.showMessageDialog(this, "Driver not available !");
                reset();
                return;
            } else if (Manager.checkDriverInTrip(id)) {
                JOptionPane.showMessageDialog(this, "Driver in trip");
                reset();
                return;
            }
            Drivers driver = Manager.getDriver(id);
            driver.setName(name);
            driver.setLicense(bangcap);
            driver.setExperiences(exper);
            driver.setPhonenumber(phoneNumber);
            driver.setAddress(address);
            Manager.updateDrivers();
            JOptionPane.showMessageDialog(this, "Saved successfully !");
            reset();
        } catch (Exception ex) {
            Logger.getLogger(EditDrivers.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
