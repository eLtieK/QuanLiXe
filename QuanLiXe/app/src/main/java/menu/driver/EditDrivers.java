    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            if ("".equals(ID.getText()) || !checkSo(ID.getText())) {
                JOptionPane.showMessageDialog(this, "Invalid input");
                return;
            }
            int id = Integer.parseInt(ID.getText());
            if (!Manager.checkValidDriver(id)) {
                JOptionPane.showMessageDialog(this, "Driver not available");
                return;
            }
            Drivers driver = Manager.getDriver(id);
            Name.setText(driver.getName());
            License.setSelectedIndex(Drivers.index_license(driver.getLicense()));
            Exper.setText(String.valueOf(driver.getExperiences()));
            PhoneNumber.setText(driver.getPhonenumber());
            Address.setText(driver.getAddress());
        } catch (Exception ex) {
            Logger.getLogger(EditDrivers.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

